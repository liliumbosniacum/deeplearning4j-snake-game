package com.lilium.snake.game.helper;

/**
 * Enum containing possible directions player can take.
 *
 * @author mirza
 */
public enum Direction {
    /**
     * Player will move up.
     */
    UP,
    /**
     * Player will move right.
     */
    RIGHT,
    /**
     * Player will move down.
     */
    DOWN,
    /**
     * Player will move left.
     */
    LEFT
}
